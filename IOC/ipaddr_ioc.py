import re

def parse_ipv4(in_ip_addr):
    '''
    description: finds all ip addresses and returns them along with where in the sentence they occured
    input: any string, mostly sentances
    output: array of schema array(array(ip address, span location of ip address))
    '''
    re_ipv4 = r'(?:(?:1\d\d|2[0-5][0-5]|2[0-4]\d|0?[1-9]\d|0?0?\d)\.){3}(?:1\d\d|2[0-5][0-5]|2[0-4]\d|0?[1-9]\d|0?0?\d)'
    #re_port = r'^([0-9]{1,4}|[1-5][0-9]{4}|6[0-4][0-9]{3}|65[0-4][0-9]{2}|655[0-2][0-9]|6553[0-5])$'
    p = re.compile(re_ipv4)
    ipv4 = [[m.group(),m.span()] for m in p.finditer(in_ip_addr)]
    return ipv4

